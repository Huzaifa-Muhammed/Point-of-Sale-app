
class Employee {
  final String name;
  final String city;
  final String contact;
  final String position;
  final String access;
  final String email;
  final String password;

  Employee({
    required this.name,
    required this.email,
    required this.password,
    required this.city,
    required this.contact,
    required this.position,
    required this.access,
  });
}